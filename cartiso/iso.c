#define Z  2

#define Mag(x,y,z)  (sqrt( (x)*(x) + (y)*(y) + (z)*(z) ))

#define calc_interp_scale(d1, d2, d) \
( ((d) - (d1))/((d2) - (d1)) )

#define interp(s, a1, a2) \
  ( (1 - (s))*(a1) + (s)*(a2) )

